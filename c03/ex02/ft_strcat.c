#include <stdio.h>
#include <string.h>

char	*ft_strcat(char *dest, char *src)
{
	char	*c;

	c = dest;

	while (*dest != '\0')
	{
		dest++;
	}
	while (*src != '\0')
	{
		*dest = *src;
		dest++;
		src++;
	}
	*dest = '\0';
	return c;
}

int	main(void)
{
	char	dest[] = "Hello";
	char	src[] = "World";

	ft_strcat(dest, src);
	printf("%s", dest);
}
